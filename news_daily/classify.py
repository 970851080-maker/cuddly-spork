def enrich_domains(item: NewsItem) -> None:
    text = f"{item.title} {item.content or ''}"
    ordered_domains = [d for d in item.categories if d in DOMAINS_12]
    domains = set(ordered_domains)
    for kw, domain in KEYWORD_TO_DOMAIN:
        if kw and kw in text:
            domains.add(domain)
    if not domains:
        ordered_domains.append("社会文化")
        domains.add("社会文化")
    for domain in DOMAINS_12:
        if domain in domains and domain not in ordered_domains:
            ordered_domains.append(domain)
    item.categories = ordered_domains
